package com.google.uicd.backend.core.uicdactions.jsonhelper;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;
import java.io.IOException;
import java.time.Duration;

/** Serialize timeout field to int */
public class UicdDurationToIntSerializer extends JsonSerializer<Duration> {
  @Override
  public void serialize(
      Duration duration, JsonGenerator jsonGenerator, SerializerProvider serializerProvider)
      throws IOException {
    jsonGenerator.writeObject((int) duration.getSeconds());
  }
}
